router.beforeEach(function (to, from, next) {
    let user = store.state.userInfo;
    if (
        !(
            to.path == "/" ||
            to.path == "/index" ||
            to.path == "/businessList" ||
            to.path == "/businessInfo" ||
            to.path == "/login" ||
            to.path == "/filter" ||
            to.path == "/order" ||
            to.path == "/register"
        )
    ) {
        if (user == null) {
            router.push("/login");
            console.log("daozhele");
            location.reload();
        }
    }
    next();
});

